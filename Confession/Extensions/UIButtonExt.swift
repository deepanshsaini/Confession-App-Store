import UIKit

extension UIButton{
    
    func selectedColor(){
       self.backgroundColor = #colorLiteral(red: 0.4500938654, green: 0.9813225865, blue: 0.4743030667, alpha: 1)
        
        
    }
    
    func setDeselectedColor(){
        
        self.backgroundColor = #colorLiteral(red: 0.4274509804, green: 0.737254902, blue: 0.3882352941, alpha: 1)
    
    }
    
    
    
    
    
    
    
}







